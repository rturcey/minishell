#include "minishell.h"

void	pipe_checks(t_sh *sh, int *i)
{
	int		end;

	end = find_end(sh->in, *i);
	if ((--end >= 0 && sh->in[end] == '|'))
		sh->obj->pip = IS_PIPE;
}
